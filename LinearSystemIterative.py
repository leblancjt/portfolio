import math
# Function for Jacobi Iterative System.
# inputs: matrix A, vector b, max iterations mi, tolerance t.
def jacobi(A, b, mi, t):
  n = len(b)
  x = [0 for i in range(n)]
  xnew = [0 for i in range(n)]

  for its in range(1, mi):
    for i in range(n):
      s = b[i]
      for j in range(i):
        s -= A[i][j] * x[j]
      for k in range(i + 1, n):
        s -= A[i][k] * x[k]
      xnew[i] = s / A[i][i]
    sum = 0
    for i in range(n):
      sum += (xnew[i] - x[i]) ** 2
    if (math.sqrt(sum) < t):
      return ('Convergence at ' + str(its) + ' Iterations.' + '\n' + ' Solution: ' + str(xnew))
    else:
      for i in range(n):
        x[i] = xnew[i]
  return ('Convergence did not occur after ' + str(mi) + ' Iterations.')

